    public void saveTransaction(String name, String fromCurrency, String currencyToBeConverted,
                            double exchangeRate, double finalCurrency) {

    String url = "jdbc:sqlite:/home/tharanetharan/Downloads/ForeignMoneyExchange/database/forex.db";

    String sql = "INSERT INTO forex_transaction (name, from_currency, to_currency, exchange_rate, final_currency) VALUES (?, ?, ?, ?, ?)";

    try {
        Connection conn = DriverManager.getConnection(url);

        PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setString(1, name);
        pstmt.setString(2, fromCurrency);
        pstmt.setString(3, currencyToBeConverted);
        pstmt.setDouble(4, exchangeRate);
        pstmt.setDouble(5, finalCurrency);

        pstmt.executeUpdate();

        pstmt.close();
        conn.close();

        System.out.println("Transaction saved successfully");

    } catch (Exception e) {
        e.printStackTrace();
    }
}
